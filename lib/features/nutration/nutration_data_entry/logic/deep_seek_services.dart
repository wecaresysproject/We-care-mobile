تنبيه L4 (منخفض): تنبيه بأن الأدوية الحالية قد تسبب "خمولاً مؤقتاً" أو "تقليل القدرة على التحمل" (Endurance)، مما يستدعي خفض شدة التمرين في الأيام الأولى.
