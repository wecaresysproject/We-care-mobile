2. شبه مطابق (استنزاف): عند فشل المطابقة التامة، البحث عن الأقرب هيكلياً في المصدر الأعلى. مثال: بسطرمة → "Beef, cured, dried" في Foundation أو "Beef jerky" في SR Legacy.
