    - تحقق 2: هل FDC ID ≠ "مركب" أو "مخلوط" أو "composite"؟
    - تحقق 3: هل وصف USDA لا يحتوي على "mixed" أو "with"؟
    - إذا فشل أي تحقق: عد إلى الخطوة 2.1 لهذا المكون
