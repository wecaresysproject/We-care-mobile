المحور (1): الأدوية الحالية 
الهدف التحليلي:
إجراء فحص التفاعلات الدوائية البينية (Inter-Drug Analysis): كشف التعارضات المباشرة بين كافة الأدوية الموجودة في القائمة الحالية (المحور 1) وبينها وبين رواسب الأدوية المنتهية (المحور 2).
تقييم "العبء الدوائي التراكمي": رصد الحالات التي يشترك فيها دواءان أو أكثر في التأثير على عضو واحد (مثل الكبد أو الكلى) مما قد يسبب سمية حتى لو كانت جرعة كل دواء منففرداً سليمة.
رصد التضاد والتعطيل العلاجي: التأكد من أن الأدوية الحالية لا يبطل بعضها مفعول بعض (Antagonism) أو تسرع تكسير بعضها في الكبد، مما يؤدي لفشل الخطة العلاجية القائمة.
2- قواعد الترجيح الزمني:
الأدوية المستمرة: تُعامل بوزن كامل (100%) طالما أن المريض يتناولها حالياً.
3- الربط التقاطعي الإلزامي:
(المحور 1) + (المحور 1): فحص التفاعلات بين المواد الفعالة داخل القائمة نفسها (تآزر أو تضاد).
(المحور 1) + (المحور 4): مقارنة "مجموع" الأدوية التي ترهق عضواً معيناً مع نتائج تحاليل ذلك العضو (نشطة <= 30 يوماً).
(المحور 1) + (المحور 3): التحقق من أن الاضطرابات في القياسات الحيوية الحالية (مثل انخفاض النبض أو ارتفاع الضغط) ليست "أثراً جانبياً" ناتجاً عن تداخل الأدوية الحالية.
(المحور 1) + (المحور 11): تتبع التداخلات السلوكية والعصبية الناتجة عن دمج الأدوية، مع التركيز على مصفوفة الـ 14 اضطراباً.
4- محفزات التحذير (التعديل للتدقيق):
L1 (حرج): اكتشاف تعارض مهدد للحياة بين دواءين حاليين (مثل خطر نزيف حاد أو توقف تنفس ناتج عن دمجهما معاً).
L2 (مرتفع): وجود تداخل بين الأدوية الحالية يؤدي لآثار جانبية شديدة تتطلب تدخلاً طبياً لتعديل الخطة أو تغيير أحد البدائل.
L3 (متوسط): تداخل يقلل من كفاءة الامتصاص أو فعالية أحد الأدوية في القائمة (خطر فشل علاجي صامت).
L4 (منخفض): وجود آثار جانبية بسيطة مشتركة (مثل جفاف الفم أو الصداع) ناتجة عن الجمع بين عدة أدوية، تتطلب "المراقبة" فقط.
L5 (احترازي): نصيحة تنظيمية لتحسين كفاءة النظام الحالي (مثل إعادة تنظيم مواعيد الجرعات للفصل بين الأدوية المتعارضة في الامتصاص).
المحور (2): الأدوية المنتهية خلال الثلاث أشهر الماضية
الهدف التحليلي:
رصد "الرواسب الكيميائية" النشطة: كشف الآثار المتبقية للأدوية المنتهية وتفاعلها مع الأدوية الحالية (المحور 1)، وليس فقط دواءً جديداً.
تحليل "فترة التطهير" (Wash-out Period): التأكد من أن المريض لم يبدأ أدوية المحور (1) قبل زوال أثر أدوية المحور (2) المتعارضة معها.
تفسير الحالة الراهنة: ربط الأدوية المنتهية بالأعراض الحالية أو نتائج التحاليل (المحور 4) لفهم ما إذا كان الاضطراب الحالي هو "بقايا أثر" لدواء سابق.
قواعد الترجيح الزمني: 
قاعدة التلاشي: تقل قوة تأثير الدواء في هذا المحور تدريجياً كلما زاد عدد الأيام منذ تاريخ انتهاء الاستخدام.
الوزن النسبي: الدواء المنتهي منذ (1-15 يوماً) يُعامل بوزن تأثير عالٍ (80%)، بينما الدواء المنتهي منذ (60-90 يوماً) يُعامل بوزن تأثير منخفض (10%)، إلا إذا كانت المادة الفعالة معروفة بقدرتها على الترسب الطويل في الأنسجة.
3- الربط التقاطعي الإلزامي:
(المحور 2) + (المحور 1): تدقيق التفاعلات المتأخرة بين رواسب الدواء المنتهي والأدوية الحالية.
(المحور 2) + (المحور 4): التأكد من أن نتائج التحاليل الحالية (الكبد/الكلى) ليست متأثرة بسمية دواء منتهٍ مؤخراً، مما قد يجعل الأدوية الحالية أكثر خطورة.
(المحور 2) + (المحور 7 - الشكاوى الطارئة): التحقق مما إذا كانت شكوى المريض الحالية (مثل الصداع أو الأرق) هي "أعراض انسحابية" لدواء توقف عنه في المحور (2).
4- محفزات التحذير (التعديل للتدقيق الشامل):
تنبيه L1 (حرج): اكتشاف أن المريض يتناول "حالياً" دواءً (المحور 1) يتفاعل بشكل قاتل مع بقايا دواء منتهٍ (المحور 2) لم يخرج من الجسم بعد (تداخل كيميائي مهدد للحياة).
تنبيه L2 (مرتفع): إذا كانت الأدوية المنتهية قد غيرت إنزيمات التمثيل الغذائي، مما يجعل الأدوية الحالية (المحور 1) تصل لمستويات سمية في الدم "الآن".
تنبيه L3 (متوسط): وجود تداخل بين الأثر المتبقي للدواء القديم وأثر الأدوية الحالية يزيد من حدة الآثار الجانبية (مثل تضاعف خطر السيولة نتيجة وجود رواسب دواء منتهٍ مع دواء سيولة حالي).
تنبيه L4 (منخفض): احتمال وجود تأثيرات طفيفة متبقية تقلل من امتصاص أو فعالية النظام الدوائي الحالي.
تنبيه L5 (احترازي): ملحوظة طبية تفيد بأن الحالة التي عولجت بالدواء المنتهي قد تتأثر أو تتداخل مع الأدوية الحالية.
المحور (3): القياسات الحيوية
 الهدف التحليلي:
تقييم مأمونية الاستمرار في الأدوية الحالية: التأكد من أن القياسات الحيوية "النشطة" للمريض لا تتعارض مع الآثار الجانبية للأدوية التي يتناولها بالفعل (مثلاً: التأكد من أن دواءً يخفض النبض ليس هو السبب في قراءة نبض منخفضة حالية).
رصد الآثار الجانبية المحققة: اكتشاف ما إذا كان اضطراب القياسات الحيوية (الضغط، السكر، النبض) هو "نتيجة فعلية" لتداخل الأدوية الحالية مع بعضها.
2- قواعد الترجيح الزمني (التعديل الجوهري):
القاعدة الذهبية (الـ 30 يوماً): تُعتبر القياسات الحيوية "نشطة" ولها وزن تأثير كامل (100%) إذا كان تاريخها <= 30 يوماً. يُبنى عليها تحذيرات L1 و L2 فوراً.
القاعدة التاريخية (> 30 يوماً): تفقد القراءات صفتها كـ "حالة راهنة" وتتحول إلى "بيانات تاريخية" لرسم الأساس الحيوي (Baseline) فقط. يُمنع بناء تحذير حرج (L1/L2) عليها، ويتم تصعيد التنبيه كـ L5 (احترازي) لطلب تحديث القياس.
فئة الحالة اللحظية (الحرارة، الأكسجين): تبقى قاعدة الـ 72 ساعة كما هي لضمان الدقة الفائقة.
3- الربط التقاطعي الإلزامي:
(المحور 3 - القياسات النشطة) + (المحور 1 - الأدوية الحالية): التحقق من أن الأدوية الحالية لا تسبب انحرافاً خطيراً في القياسات الحيوية المأخوذة خلال الـ 30 يوماً الأخيرة.
(المحور 3 - BMI) + (المحور 1): استخدام مؤشر كتلة الجسم لتقييم مدى ملاءمة جرعات الأدوية الحالية (خاصة في حالات السمنة المفرطة أو النحافة لضمان عدم حدوث سمية تراكمية).
(المحور 3 - السكر) + (المحور 16 - الغذاء): ربط استقرار قراءات السكر (النشطة) بنمط استهلاك الكربوهيدرات المسجل.
4- محفزات التحذير (التعديل للتدقيق):
تنبيه L1 (حرج): وجود قراءة "نشطة" (<= 30 يوماً) تمثل خطراً داهماً مع استمرار أحد الأدوية الحالية (مثل مريض ضغطه الحالي 80/50 ولا يزال يتناول دواءً يخفض الضغط).
تنبيه L2 (مرتفع): وجود "نمط" مستقر من القراءات المضطربة خلال الـ 30 يوماً الأخيرة يجعل استمرار النظام الدوائي الحالي خطراً على استقرار الحالة.
تنبيه L3 (متوسط): تذبذب واضح في القراءات النشطة يتطلب مراجعة الطبيب لتعديل جرعات الأدوية الحالية.
تنبيه L4 (منخفض): الحاجة لمراقبة قياس حيوي معين لأن الأدوية الحالية تسبب اضطراباً طفيفاً فيه (مثل جفاف بسيط أو تذبذب طفيف في السكر).
تنبيه L5 (احترازي): (يُفعل هنا بقوة) عند وجود قياسات تجاوزت 30 يوماً؛ حيث يوجه المحرك نصيحة بضرورة تحديث القياس قبل الحكم على سلامة الأدوية الحالية، ولا يصدر تحذيراً حرجاً.
المحور (4): التحاليل الطبية
(خلال آخر 365 يوم - ثلاث قراءات لكل تحليل)
الهدف التحليلي:
تقييم الكفاءة الوظيفية الحالية للأعضاء: التأكد من أن الأدوية التي يتناولها المريض "الآن" (المحور 1) لا تسبب ضرراً صامتاً للكبد أو الكلى بناءً على نتائج التحاليل.
تحليل الاتجاه (Trend Analysis): استخدام القراءات الثلاث للتنبؤ بما إذا كان النظام العلاجي الحالي يؤدي لتحسن أو تدهور وظائف الجسم بمرور الوقت.
كشف التعارضات المخبرية النشطة: تحديد ما إذا كان استمرار المريض على دواء معين يمثل خطراً بناءً على أحدث نتائج المختبر.
2- قواعد الترجيح الزمني (التعديل المتوافق مع الـ 30 يوماً):
التحاليل الحادة/المتغيرة (مثل INR، البوتاسيوم، الهيموجلوبين): تُطبق قاعدة الـ 30 يوماً الصارمة. إذا تجاوزت التاريخ، تُعامل كبيانات تاريخية (وزن 0% للقرار الحرج) ويُفعل التنبيه L5.
التحاليل الوظيفية (مثل وظائف الكبد والكلى): تظل "نشطة" لمدة 90 يوماً، ولكن يُعطى الوزن الأكبر (100%) لما هو ضمن الـ 30 يوماً الأخيرة.
التحاليل النمطية (مثل السكر التراكمي HBA1c، الغدة): تظل صالحة لمدة 180 يوماً لتعكس استقرار بيئة الجسم.
3- الربط التقاطعي الإلزامي:
(المحور 4) + (المحور 1): هل الاضطراب الحالي في التحاليل (مثل ارتفاع الكرياتينين) هو نتيجة مباشرة أو "أثر جانبي محقق" لتناول الأدوية الحالية؟
(المحور 4) + (المحور 6 - الأمراض المزمنة): تقييم مدى سيطرة النظام العلاجي الحالي على المرض المزمن (مثل مريض ضغط وتحاليله تظهر تأثراً في وظائف الكلى).
(المحور 4) + (المحور 16 - الغذاء): ربط نقص العناصر (مثل البوتاسيوم) بنوع الأدوية الحالية (مثل المدرات) والنظام الغذائي.
4- محفزات التحذير (التعديل للتدقيق):
تنبيه L1 (حرج): قراءة تحليلية "نشطة" (<= 30 يوماً) تشير لفشل وظيفي حاد يمنع استمرار أحد الأدوية الحالية (مثل GFR منخفض جداً مع دواء يطرح عن طريق الكلى).
تنبيه L2 (مرتفع): القراءات الثلاث تظهر اتجاهاً تدهورياً واضحاً (Trend) ناتجاً عن استمرار المريض على نظامه العلاجي الحالي.
تنبيه L3 (متوسط): انحراف بسيط في التحاليل النشطة يشير إلى أن الأدوية الحالية تضعف كفاءة العضو أو تسبب آثاراً جانبية مزعجة.
تنبيه L4 (منخفض): قراءة "حدّية" (Borderline) تتطلب توصية المريض بإعادة التحليل قريباً لمراقبة أثر الأدوية الحالية.
تنبيه L5 (احترازي): (تفعيل قاعدة البيانات القديمة)؛ طلب تحديث التحاليل لأن البيانات المتاحة تجاوزت المدد المسموحة (مثلاً: لا يوجد تحليل وظائف كبد منذ 4 أشهر والمريض يتناول أدوية تؤثر عليه).
المحور (5): العمليات الجراحية
الهدف التحليلي:
رصد المخاطر القائمة بين الأدوية الحالية والتبعات الجراحية: التأكد من أن الأدوية التي يتناولها المريض "الآن" (المحور 1) لا تسبب مضاعفات ناتجة عن تغييرات تشريحية سابقة (مثل دواء يؤثر على امتصاص الفيتامينات لمريض أجرى "تكميم معدة" منذ سنوات).
التدقيق مع الغرسات والأجهزة الطبية: التأكد من أن النظام الدوائي الحالي لا يتعارض مع "الدعامات" أو "صمامات القلب" أو "المفاصل الصناعية" المسجلة في ملف المريض.
قواعد الترجيح الزمني:
الجراحات الكبرى/المستديمة (مثل استئصال عضو، جراحات القلب، زراعة الأعضاء): تُعامل بوزن كامل (100%) للأبد، لأن أثرها على كيمياء وتكوين الجسم دائم.
الجراحات الحديثة (آخر 90 يوماً): تُعامل بوزن مرتفع جداً بسبب مخاطر فترة "الاستشفاء" (مثل مخاطر التجلط أو الالتهاب بعد الجراحة).
الجراحات الصغرى القديمة: يقل وزنها تدريجياً في التحليل ما لم تكن مرتبطة مباشرة بمكان عمل الدواء الجديد.
3- الربط التقاطعي الإلزامي:
(المحور 5) + (المحور 1): هل الأدوية الحالية تسبب ضغطاً إضافياً على عضو تم إجراء جراحة فيه (مثل دواء يرهق الكلية الوحيدة المتبقية لمريض أجرى استئصالاً)؟
(المحور 5) + (المحور 4 - التحاليل النشطة): ربط الجراحة السابقة بنتائج التحاليل الحالية (<= 30 يوماً) لتقييم مدى كفاءة العضو في التعامل مع الأدوية الحالية.
4- محفزات التحذير (التعديل للتدقيق):
تنبيه L1 (حرج): اكتشاف أن المريض يتناول "حالياً" دواءً ممنوعاً قطعيًا بعد نوع معين من الجراحات (مثل تناول مسكنات NSAIDs بانتظام لمريض أجرى جراحة "تحويل مسار معدة" مما يهدد بحدوث قرحة ونزيف فوري).
تنبيه L2 (مرتفع): الأدوية الحالية تؤثر سلباً على عضو أجرى فيه جراحة "حديثة" (آخر 90 يوماً) مما يعيق الالتئام أو يسبب فشلاً وظيفياً (مثل أدوية السيولة الحالية لمريض أجرى جراحة كبرى مؤخراً دون مراجعة الجرعة).
تنبيه L3 (متوسط): احتمال ضعف امتصاص الأدوية الحالية بسبب جراحات سابقة في الجهاز الهضمي، مما يشير إلى "فشل علاجي محتمل" يتطلب تغيير الشكل الصيدلاني (من أقراص إلى سائل أو حقن).
تنبيه L4 (منخفض): الحاجة لمراقبة مكان جراحة قديم بسبب آثار جانبية طفيفة للأدوية الحالية (مثل آلام في المفاصل الصناعية أو الدعامات).
تنبيه L5 (احترازي): (لا يوجد تغيير - نصيحة وقائية عامة مرتبطة بنوع الجراحة وتأثيرها البعيد على الأدوية).
المحور (6): الأمراض المزمنة
الهدف التحليلي:
تحديد "الموانع المرضية الحالية": التأكد من أن الأدوية التي يتناولها المريض "الآن" (المحور 1) لا تسبب تفاقماً لطبيعة أمراضه المزمنة المسجلة (مثلاً: مريض ربو يتناول دواءً للضغط من فئة Beta-blockers يسبب له تضيقاً شعبياً مستمراً).
تقييم "السيطرة العلاجية": فحص ما إذا كانت الأدوية الحالية تنجح في إبقاء المرض المزمن "مستقراً" أم أن هناك خللاً في النتائج الحيوية (المحور 3) والتحاليل (المحور 4).
كشف "التآزر المرضي السلبي": رصد الأدوية التي تعالج عرضاً في جهة وتؤدي لتفاقم مرض مزمن في جهة أخرى (مثل الكورتيزون لعلاج الالتهاب الذي يرفع السكر لمريض السكري بصفة دائمة).
2- قواعد الترجيح الزمني (Temporal Logic):
وزن الحالة الثابت: المرض المزمن بحد ذاته له وزن تأثير دائم (100%) طالما لم يتم تحديث الحالة بـ "الشفاء التام"، لأنه يمثل بيئة العمل الأساسية للأدوية.
ديناميكية الحالة (الحداثة): يتم ترجيح مخاطر المرض بناءً على "آخر حالة" (Status) مسجلة (مستقر، نشط، غير منضبط). الحالة "النشطة" ترفع حساسية البروتوكول للتحذيرات من الدرجة L1 و L2 فوراً.
قاعدة الربط الحيوي (الـ 30 يوماً): عند ربط المرض المزمن بالقياسات (المحور 3)، يتم الاعتماد فقط على القراءات التي لا يتجاوز عمرها 30 يوماً لتصنيف المرض كـ "غير منضبط" وبناء تحذير حرج. القراءات الأقدم من ذلك تضعف وزن التحذير وتحوله إلى L5 (طلب تحديث بيانات).
3- الربط التقاطعي الإلزامي:
(المحور 6) + (المحور 3 - القياسات النشطة <= 30 يوماً): الربط بين التشخيص المزمن والقراءات الحيوية الأخيرة؛ فإذا كان التشخيص (ضغط دم) والقراءة (160/100)، يُصنف النظام العلاجي الحالي كـ "فشل في السيطرة".
(المحور 6) + (المحور 1 - الأدوية الحالية): التأكد من أن قائمة الأدوية تحتوي على العلاج القياسي للمرض المزمن، أو أنها لا تحتوي على أدوية تزيد من أعراضه كأثر جانبي.
(المحور 6) + (المحور 4 - التحاليل): ربط المرض بنتائج المختبر الحديثة (مثل مريض فشل كلوي مزمن مع قراءة GFR منخفضة في المحور 4) لتقييم مدى تحمل الجسم للأدوية الحالية.
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): اكتشاف تناول دواء حالي محظور تماماً للمرض المزمن المسجل (مثل إعطاء Beta-blockers لمريض يعاني من ربو شعبي حاد ونشط).
تنبيه L2 (مرتفع): النظام العلاجي الحالي يسبب "اختلال توازن" (Decompensation) للمرض المزمن (مثل دواء يرفع ضغط العين لمريض يعاني من "جلوكوما/مياه زرقاء").
تنبيه L3 (متوسط): الحاجة لتعديل جرعات أدوية المرض المزمن في المحور الأول لأن أحد الأدوية الأخرى (لعلاج عرض مختلف) يؤثر على استقراره (مثل دواء يرفع السكر يستلزم زيادة جرعة الإنسولين).
تنبيه L4 (منخفض): الحاجة لمراقبة أعراض المرض المزمن بشكل أدق بسبب تداخلات طفيفة ناتجة عن "العبء الدوائي الكلي" الحالي.
تنبيه L5 (احترازي): نصيحة توعوية، أو تحذير من "قِدم البيانات"؛ كأن يكون المريض مصاباً بالضغط ولا توجد قراءة منذ > 30 يوماً، مما يستدعي طلب التحديث لتقييم أمان الأدوية الحالية.
المحور (7): الشكاوى الطارئة - آخر 6 أشهر (Acute Complaints/Symptoms)
 الهدف التحليلي:
كشف الآثار الجانبية للأدوية الحالية: التمييز ما إذا كانت الشكوى الطارئة (مثل السعال، الأرق، أو ألم المعدة) قد بدأت بالتزامن مع أحد أدوية المحور (1)، وتصنيفها كأثر جانبي محقق للنظام العلاجي القائم.
منع "تغطية الأعراض" (Symptom Masking): التأكد من أن الأدوية الحالية لا تخفي أعراضاً لشكوى طارئة خطيرة تحتاج فحصاً سريرياً (مثل تناول مسكنات قوية بانتظام تخفي آلام التهاب مرارة أو قرحة نشطة).
رصد التفاقم الكيميائي: ضمان أن الأدوية الحالية (المحور 1) لا تزيد من حدة شكوى طارئة نشطة (مثل دواء يرفع النبض لمريض يشكو من خفقان طارئ).
2- قواعد الترجيح الزمني (Temporal Logic):
الشكاوى النشطة (آخر 7 أيام): تُعامل بوزن كامل (100%) لأنها تمثل الحالة الفسيولوجية الراهنة وتفاعلها المباشر مع الأدوية الحالية.
الشكاوى المتوسطة (من شهر إلى 3 أشهر): يُخفض وزنها إلى (50%) وتُستخدم للبحث عن "النمط الدوري" (هل تتكرر الشكوى مع مواعيد جرعات معينة؟).
الشكاوى القديمة (من 3 إلى 6 أشهر): تُعامل كبيانات مرجعية (10%) للمقارنة فقط، وتفقد قيمتها كتحذير للحالة الراهنة ما لم تكن مرتبطة بمرض مزمن في المحور السادس.
3- الربط التقاطعي الإلزامي:
(المحور 7) + (المحور 1): مقارنة "تاريخ بدء الشكوى" مع "تاريخ بدء الدواء الحلي"؛ فإذا تطابقا، يتم رفع درجة التحذير لوجود علاقة سببية.
(المحور 7) + (المحور 3 - القياسات النشطة <= 30 يوماً): ربط الشكوى (مثل الدوار) بالقياس الحيوي (مثل ضغط دم منخفض) وبالدواء الحالي (مثل مدرات البول).
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): الشكوى الطارئة الحالية (مثل ألم صدري أو ضيق تنفس حاد) تمثل مؤشراً لخطورة عالية تتفاقم كيميائياً بسبب أحد الأدوية الحالية (المحور 1).
تنبيه L2 (مرتفع): اكتشاف أن الشكوى الطارئة الحالية هي "أثر جانبي شائع جداً" لأحد أدوية المحور (1)، مما يستدعي مراجعة الطبيب لاستبدال الدواء بدلاً من مجرد علاج العرض.
تنبيه L3 (متوسط): الشكوى الطارئة تدل على عدم تحمل (Intolerance) لأحد الأدوية الحالية، مما يقلل من جودة حياة المريض ويحتاج لتعديل الجرعة.
تنبيه L4 (منخفض): توصية المريض بمراقبة نمط الشكوى (هل تزداد بعد تناول الجرعة مباشرة؟) للتأكد من وجود علاقة سببية مع النظام العلاجي الحالي.
تنبيه L5 (احترازي): ملحوظة للمريض بأن الشكوى المسجلة (منذ أكثر من شهر) قد تكون مرتبطة بنقص في البيانات الحيوية، ويجب استشارة الطبيب إذا استمرت رغم الالتزام بالعلاج.
المحور (8): الأمراض الوراثية المتوقعة
الهدف التحليلي:
كشف "المحفزات الجينية" (Genetic Triggering): التأكد من أن الأدوية الحالية (المحور 1) لا تعمل كـ "مُحفز" (Inducer) لظهور أمراض يمتلك المريض استعداداً وراثياً لها (مثل دواء يرفع سكر الدم لمريض لديه تاريخ عائلي صارخ بالسكري النوع الثاني).
تطبيق "مبدأ الحيطة الدوائية": معاملة المريض ذو الاستعداد الوراثي العالي (مثل تاريخ جلطات مبكرة في العائلة) بحذر يقترب من معاملة المريض المصاب فعلياً، لضمان عدم تسريع ظهور المرض.
رصد "التناقض الوقائي": التأكد من أن الأدوية التي يتناولها المريض لعلاج عرض بسيط لا تدمر "خط الدفاع الوراثي" لديه (مثل أدوية ترهق الكبد لمريض لديه تاريخ عائلي بمرض تليف الكبد المناعي).
2- قواعد الترجيح الزمني (Temporal Logic):
الثبات المطلق: هذه البيانات ثابتة (100%) ولا تخضع للاضمحلال الزمني؛ فالبصمة الوراثية لا تتغير.
قاعدة "الارتباط العمري": تزداد قوة التحذير في هذا المحور طردياً مع تقدم السن (الربط مع المحور 0)؛ فكلما اقترب المريض من السن الذي ظهر فيه المرض لدى أقاربه، زادت حساسية البروتوكول للتحذيرات من الدرجة L1 و L2.
3- الربط التقاطعي الإلزامي (الأشمل):
(المحور 8) + (المحور 1 - الأدوية الحالية): فحص الآثار الجانبية "طويلة الأمد" للأدوية الحالية ومقارنتها بالخريطة الوراثية للمريض.
(المحور 8) + (المحور 4 - التحاليل النشطة): إذا كان هناك استعداد وراثي لمرض معين (مثل الفشل الكلوي)، يتم تدقيق نتائج التحاليل (<= 30 يوماً) بمنتهى الصرامة لرصد أي "بدايات" خلل قد تسببها الأدوية الحالية.
(المحور 8) + (المحور 16 - الغذاء) + (المحور 18 - الرياضة): تقييم ما إذا كان نمط الحياة الحالي يساعد في "إخماد" الجينات السلبية أم "تنشيطها" بالتزامن مع الأدوية.
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): اكتشاف تناول دواء حالي يسرع بشكل قطعي ومباشر من ظهور مرض وراثي خطير (مثل مريض لديه استعداد وراثي لنوع معين من السرطانات ويتناول دواءً يُصنف كـ "مسرطن محتمل" لهذا النوع تحديداً).
تنبيه L2 (مرتفع): الأدوية الحالية لها أثر جانبي يطابق تماماً "نقطة الضعف الوراثية" للمريض (مثل دواء يرفع الكوليسترول لمريض لديه تاريخ عائلي بجلطات قلبية في سن الثلاثين).
تنبيه L3 (متوسط): الاستعداد الوراثي يتطلب زيادة وتيرة الفحوصات الدورية (Screening) طالما أن المريض مستمر على نظامه الدوائي الحالي (مثل طلب فحص قاع العين بانتظام).
تنبيه L4 (منخفض): نصائح بتعديل "نمط الحياة" لتقليل احتمالية تقاطع الأدوية الحالية مع الجينات الوراثية السلبية.
تنبيه L5 (احترازي): ملحوظة تعليمية تربط بين التاريخ العائلي والأدوية الحالية، لزيادة وعي المريض بالأعراض المبكرة التي يجب مراقبتها.
المحور (9): الحساسية 
 الهدف التحليلي:
كشف "التحسس الدوائي النشط": التأكد من أن الأدوية التي يتناولها المريض "الآن" (المحور 1) ليست هي المصدر الخفي لشكاوى جلدية أو تنفسية طارئة (المحور 7).
تدقيق "المكونات غير الفعالة" (Excipients Audit): فحص المواد المضافة في الأدوية الحالية (مثل اللاكتوز، الجلوتين، أو الأصباغ) ومقارنتها بسجل الحساسية الغذائية أو البيئية للمريض.
رصد "الحساسية التصالبية" القائمة: التأكد من أن النظام الدوائي الحالي لا يجمع بين أدوية تنتمي لعائلات كيميائية تسبب تحسساً تبادلياً (Cross-Reactivity).
2- قواعد الترجيح الزمني (Temporal Logic):
وزن الأبدية (الثبات المناعي): الحساسية الدوائية الموثقة تظل بوزن تأثير (100%) مدى الحياة؛ فالذاكرة المناعية لا تنسى المحفزات، والخطأ فيها قد يكون قاتلاً.
الحساسية الموسمية/البيئية: يظل وزنها مفعلًا (80%) خلال مواسم معينة، وتُستخدم لتدقيق الأدوية الحالية التي تؤثر على الجهاز التنفسي أو الجلد لتجنب "تراكم التهيج".
3- الربط التقاطعي الإلزامي:
(المحور 9) + (المحور 1 - الأدوية الحالية): فحص التداخل المناعي بين كافة الأدوية الحالية وسجل الحساسية التاريخي للمريض.
(المحور 9) + (المحور 7 - الشكاوى الطارئة): ربط الشكاوى الجلدية (طفح، حكة) أو التنفسية الحالية باحتمالية وجود حساسية "غير مكتشفة" أو "تطورت حديثاً" تجاه دواء مستمر في المحور الأول.
(المحور 9) + (المحور 16 - الغذاء): التحقق من الحساسية الغذائية المسجلة ومقارنتها بالمواد الحاملة (Excipients) في الأدوية الحالية (مثلاً: مريض حساسية قمح يتناول دواءً يحتوي على نشأ القمح كمادة مالئة).
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): اكتشاف أن المريض يتناول "حالياً" دواءً يحتوي على مادة فعالة لديه حساسية مؤكدة منها، أو وجود خطر "صدمة حساسية" (Anaphylaxis) وشيك.
تنبيه L2 (مرتفع): وجود احتمال قوي للحساسية التصالبية بين الأدوية الحالية (مثل تناول دواء من عائلة "السيفالوسبورين" لمريض لديه حساسية موثقة من "البنسلين").
تنبيه L3 (متوسط): وجود تعارض بين "المواد الإضافية" في الأدوية الحالية وسجل الحساسية الغذائية/الكيميائية للمريض، مما يستدعي استبدال المنتج التجاري بآخر من شركة مختلفة بتركيبة إضافية سليمة.
تنبيه L4 (منخفض): وجود تاريخ من "عدم التحمل" (Intolerance) لأحد الأدوية الحالية، يتطلب مراقبة الأعراض الهضمية أو الجلدية البسيطة.
تنبيه L5 (احترازي): نصيحة للمريض بمراقبة علامات التحسس عند استمرار دواء معين معروف تاريخياً بمعدل حساسية مرتفع، خاصة إذا كانت البيانات الحيوية (المحور 3) تظهر اضطراباً غير مفسر.
المحور (10): التطعيمات - آخر 3 أشهر
1. الهدف التحليلي:
تدقيق "الكفاءة المناعية للقاح": التأكد من أن الأدوية الحالية (المحور 1) مثل الكورتيزونات أو المثبطات المناعية لم تُفسد عملية بناء الأجسام المضادة (Immune Response) للقاحات التي تلقاها المريض في آخر 90 يوماً.
رصد "التفاعل الالتهابي التراكمي": التأكد من أن الأدوية الحالية لا تزيد من حدة الآثار الجانبية للقاح (مثل آلام المفاصل أو العضلات) أو تخفي أعراضاً هامة (مثل الحمى) قد تكون مؤشراً لرد فعل عكسي للقاح.
تحليل "النافذة المناعية" النشطة: مراقبة حالة المريض خلال فترة الاستنفار المناعي (أول 14 يوماً) لضمان عدم حدوث تداخل كيميائي مع نظامه الدوائي القائم.
2- قواعد الترجيح الزمني (Temporal Logic):
فترة النشاط الحاد (أول 14 يوماً): تُعامل بوزن كامل (100%)؛ حيث يكون الجهاز المناعي في حالة استنفار قصوى وأي دواء حالي (خاصة المثبطات) يمثل خطراً حرجاً.
فترة الاستقرار (من 15 إلى 45 يوماً): ينخفض الوزن إلى (50%)؛ وهي فترة تكوين الذاكرة المناعية الدائمة.
فترة ما بعد الـ 45 يوماً: يتلاشى الوزن تدريجياً ليصل إلى (0%) بنهاية الشهر الثالث، إلا في اللقاحات ذات الجرعات التعزيزية المتقاربة.
3- الربط التقاطعي الإلزامي:
(المحور 10) + (المحور 1 - الأدوية الحالية): البحث عن أدوية (مثل "الميثوتريكسيت" أو الكورتيزونات بجرعات عالية) قد تكون أدت لفشل التطعيم المسجل في بناء مناعة كافية.
(المحور 10) + (المحور 3 - الحرارة/النبض): إذا كانت القياسات النشطة (<= 30 يوماً) تظهر اضطراباً، يتم فحص ما إذا كان ذلك ناتجاً عن "تفاعل لقاح" متأخر أو تداخل الدواء الحالي مع اللقاح.
(المحور 10) + (المحور 7 - الشكاوى الطارئة): تصنيف آلام الجسم أو الخمول كأعراض جانبية طبيعية للقاح (إذا كانت ضمن الـ 7 أيام الأولى) لضمان عدم إضافة أدوية جديدة لا داعي لها.
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): اكتشاف أن المريض يتناول "حالياً" جرعات عالية من مثبطات المناعة تزامناً مع تلقي "لقاح حي مضعف" (Live Attenuated Vaccine)، مما يهدد بحدوث الإصابة بالمرض نفسه.
تنبيه L2 (مرتفع): النظام العلاجي الحالي يسبب تفاعلاً التهابياً أو وعائياً يتقاطع مع مخاطر اللقاح الحديث (مثل زيادة خطر التجلط أو التهابات الأنسجة في حالات نادرة جداً).
تنبيه L3 (متوسط): احتمال أن تكون الأدوية الحالية قد قللت من "جودة الاستجابة المناعية" للقاح، مما قد يستدعي فحص مستوى الأجسام المضادة أو استشارة طبيب المناعة.
تنبيه L4 (منخفض): توصية المريض بمراقبة أي تداخل بسيط في الآثار الجانبية (مثل الصداع) بين أدويته الروتينية واللقاح.
تنبيه L5 (احترازي): ملحوظة تعليمية توضح أن الشعور بالإرهاق الحالي قد يكون ناتجاً عن "الجهد المناعي" للقاح وليس فشلاً في الأدوية الحالية.
المحور (11): الصحة النفسية والقياس السلوكي 
الهدف التحليلي:
كشف "الآثار الجانبية النفسية" النشطة: تحليل ما إذا كان الخلل في أي من الـ 14 اضطراباً سلوكياً (مثل الأرق، وسواس النظافة، أو تقلب المزاج) هو "أثر جانبي" محقق للأدوية الحالية (المحور 1).
تدقيق "التداخل الأيضي-العصبي": رصد التفاعلات المعقدة بين الأدوية العضوية والأدوية النفسية (المحور 1) وتأثيرها التراكمي على "الكيمياء العصبية" للمريض.
تقييم "أهلية الامتثال" (Adherence Audit): استخدام نتائج محاور (التركيز والانتباه) و(الانضباط الذاتي) لتفسير سبب "عدم انتظام المريض" في أدويته الحالية وتقديم حلول تقنية بدلاً من زيادة الجرعات.
2- قواعد الترجيح الزمني (Temporal Logic):
التشخيص الطبي الموثق: يظل بوزن ثابت (100%) كخلفية أمان دائم.
الاضطرابات الـ 14 (دورة الـ 90 يوماً): تُعامل نتائج اختبارات WECARE SYS بوزن كامل (100%) لمدة 3 أشهر. بعد ذلك، تُصنف كبيانات تاريخية (30%) ويتم تفعيل التنبيه L5 لطلب "إعادة الاختبار" (Re-test) لأن الحالة المزاجية والسلوكية شديدة التغير.
3- مصفوفة الربط السلوكي (Audit Matrix): يجب على الـ AI ربط النظام الدوائي الحالي بالاضطرابات الـ 14 وفق المنطق التالي:
الأمان النفسي الحرج: (الأفكار الانتحارية، الأفكار الوسواسية، التحكم في الانفعالات) -> هل الأدوية الحالية تزيد من حدتها؟
الأداء الوظيفي اليومي: (التركيز والانتباه، الطاقة والنشاط والنوم، الحالة المزاجية) -> هل الأدوية الحالية هي سبب الخمول أو الأرق المسجل؟
النمط الحياتي والنمو: (الشهية والأكل، السلوك القهري، الصورة الذاتية) -> هل تغيرت شهية المريض بعد البدء بالأدوية الحالية؟
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): اكتشاف أن المريض يتناول دواءً حالياً (عضوياً أو نفسياً) يسبب "أفكاراً انتحارية أو وسواسية" كأثر جانبي، بينما سجل المريض درجة "خطيرة" في هذه المحاور باختبار WECARE.
تنبيه L2 (مرتفع): وجود خلل في (الطاقة والنشاط والنوم) ناتج بوضوح عن تداخل الأدوية الحالية، أو دواء يتطلب دقة مواعيد عالية لمريض سجل قصوراً حاداً في (التركيز والانتباه).
تنبيه L3 (متوسط): النظام العلاجي الحالي يؤثر سلباً على (الشهية والأكل) أو يسبب خمولاً يعيق (الاندماج المجتمعي) للمريض، مما يستلزم مراجعة الطبيب لتغيير البدائل.
تنبيه L4 (منخفض): توصية باستخدام أدوات مساعدة (تطبيقات تذكير) بناءً على قصور في (الانضباط الذاتي) المسجل، لضمان استقرار نتائج الأدوية الحالية.
تنبيه L5 (احترازي): (تفعيل قاعدة الـ 90 يوماً)؛ نصيحة بضرورة إعادة إجراء "اختبار WECARE السلوكي" لأن البيانات تجاوزت 3 أشهر، لضمان أن الأدوية الحالية لا تسبب انحرافاً سلوكياً صامتاً.
المحور (12): العيون 
الهدف التحليل:
حماية المريض من "التدهور البصري الصامت": التأكد من أن الأدوية التي يتناولها المريض "الآن" (المحور 1) لا تسبب ارتفاعاً في ضغط العين أو عتامة في العدسة (كآثار جانبية للكورتيزونات مثلاً).
تدقيق التفاعلات الجزيئية للقطرات: رصد التفاعلات بين قطرات العين (التي تُمتص جهازياً وتدخل الدورة الدموية) وبين قائمة الأدوية الحالية (المحور 1).
رصد "متلازمة جفاف العين الدوائي": التأكد من أن جفاف العين أو التهاب القرنية الحالي ليس ناتجاً عن تداخل أدوية الحساسية أو الأدوية النفسية التي يتناولها المريض بانتظام.
2- قواعد الترجيح الزمني (Temporal Logic):
التشخيصات المستمرة (الجلوكوما، اعتلال الشبكية): تُعامل بوزن ثابت (100%) للأبد؛ فهي تشكل "موانع استخدام" دائمة لبعض فئات الأدوية.
الأعراض العارضة (التهابات، جفاف مؤقت): تُطبق قاعدة الـ 30 يوماً الصارمة. إذا كانت الشكوى ضمن آخر 30 يوماً، تُعامل بوزن (100%) كحالة نشطة. إذا تجاوزت ذلك، تصبح "تاريخاً مرضياً" بوزن (10%) ولا يُبنى عليها تحذير حرج، بل يُطلب تحديث الحالة (L5).
3- الربط التقاطعي الإلزامي:
(المحور 12) + (المحور 6 - الأمراض المزمنة): ربط اعتلال الشبكية بضبط السكر والضغط؛ فإذا كانت التحاليل (المحور 4) غير منضبطة، تزداد حساسية العين للأدوية الحالية التي تؤثر على الأوعية الدموية.
(المحور 12) + (المحور 11 - النفسية): تدقيق الأدوية النفسية الحالية التي تسبب "زغللة" أو جفافاً، والتأكد من عدم وجود "تآزر سلبي" يضاعف هذه الآثار.
(المحور 12) + (المحور 4 - التحاليل): ربط نقص الفيتامينات (مثل A أو B12) بالأعراض البصرية الحالية المسجلة.
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): اكتشاف أن المريض يتناول "حالياً" دواءً يؤدي لرفع ضغط العين (Mydriatic effect) وهو يعاني من "جلوكوما الزاوية المغلقة" (Angle-closure glaucoma)، مما يهدد بفقدان بصر مفاجئ.
تنبيه L2 (مرتفع): النظام الدوائي الحالي يحتوي على أدوية ذات سمية معروفة للعصب البصري أو الشبكية (مثل الهيدروكسي كلوروكوين) لمريض لديه تشخيص مسبق بضعف العصب أو الشبكية.
تنبيه) L3 متوسط): الأدوية الحالية تسبب جفافاً شديداً في الأغشية المخاطية لمريض يعاني من "جفاف العين المزمن" (النشط <= 30 يوماً)، مما يستدعي تعديل الخطة أو إضافة مرطبات مكثفة.
تنبيه) L4 منخفض): رصد ضبابية مؤقتة في الرؤية ناتجة عن "العبء الدوائي الكلي" الحالي، مما يستدعي تحذير المريض من الأنشطة التي تتطلب دقة بصرية (كالقيادة).
تنبيه L5 (احترازي): (تفعيل قاعدة البيانات القديمة)؛ نصيحة بإجراء فحص قاع العين لأن المريض يتناول أدوية تؤثر على العين لفترة طويلة (> 6 أشهر) ولم يتم تحديث بيانات العين منذ فترة طويلة.
المحور (13): الأسنان والفكين
الهدف التحليلي:
رصد مخاطر "نخر عظام الفك" (Osteonecrosis): التأكد من أن الأدوية الحالية (مثل أدوية هشاشة العظام Bisphosphonates أو بعض أدوية الأورام) لا تمثل خطراً داهماً في حال خضوع المريض لإجراءات سنية تداخلية (خلع أو زراعة).
تدقيق "بيئة الالتئام" (Healing Audit): رصد الأدوية الحالية التي تؤخر التئام الجروح أو تزيد من خطر النزيف (مثل مسيلات الدم أو الكورتيزونات) وتأثيرها على جراحات الأسنان الأخيرة.
تحليل "المتلازمة الفموية الدوائية": اكتشاف ما إذا كان "جفاف الفم" (Xerostomia) أو "تضخم اللثة" هو أثر جانبي ناتج عن الأدوية الحالية (مثل أدوية الضغط أو الصرع).
2- قواعد الترجيح الزمني (Temporal Logic):
الإجراءات الجراحية النشطة (<= 30 يوماً): تُعامل بوزن كامل (100%)؛ حيث تكون مخاطر النزيف، العدوى، أو فشل الغرسات (Implants) في ذروتها، وأي دواء حالي يؤثر على المناعة أو التجلط يمثل خطراً حرجاً.
الحالات المزمنة (التهاب اللثة، أمراض مصل الفك): تظل بوزن (50%) كمرجع مستمر لتقييم مدى تأثر الأنسجة بالأدوية.
تاريخ العمليات القديم (> 30 يوماً): يُستخدم كبيانات تاريخية لفهم الحساسية من البنج أو المواد المستخدمة سابقاً، بوزن (10%).
3- الربط التقاطعي الإلزامي:
(المحور 13) + (المحور 1 - الأدوية الحالية): الربط المباشر بين أدوية السيولة أو أدوية العظام وبين أي "إجراء جراحي سني" مسجل في آخر 30 يوماً.
(المحور 13) + (المحور 9 - الحساسية): تدقيق المواد المستخدمة في حشوات الأسنان أو التخدير الموضعي (مثل Lidocaine) ومقارنتها بسجل الحساسية التاريخي للمريض.
(المحور 13) + (المحور 4 - التحاليل النشطة): ربط حالة عظام الفك وقدرة الالتئام بتحاليل الكالسيوم، فيتامين D، ومعايير التجلط (PT/INR) التي لا تتجاوز 30 يوماً.
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): اكتشاف مريض يتناول أدوية هشاشة عظام (مثل زوميت أو بروليا) وخضع/سيخضع لعملية خلع أو زراعة؛ خطر حدوث "موت عظام الفك المرتبط بالدواء" (MRONJ).
تنبيه L2 (مرتفع): النظام الدوائي الحالي (المحور 1) يسبب زيادة خطيرة في النزيف لمريض خضع لتوّه (<= 7 أيام) لخلع ضرس أو جراحة لثة.
تنبيه L3 (متوسط): اكتشاف أن "تضخم اللثة" أو "جفاف الفم الشديد" المسجل هو أثر جانبي للأدوية الحالية (مثل Phenytoin أو Nifedipine)، مما يستدعي استشارة الطبيب لتغيير البديل أو تكثيف العناية الفموية.
تنبيه L4 (منخفض): رصد آثار جانبية مؤقتة ناتجة عن الأدوية (مثل طعم معدني أو تقرحات بسيطة) وتوجيه المريض لكيفية التعامل معها.
تنبيه L5 (احترازي): نصيحة بإبلاغ طبيب الأسنان بضرورة الحذر عند استخدام تخدير يحتوي على (Epinephrine) لأن المريض يتناول أدوية معينة قد تسبب تسارعاً حاداً في ضربات القلب.
المحور (14): النظام الغذائي
تحليل 34 عنصراً غذائياً وتأثيرها التقاطعي:
الهدف التحليلي:
تدقيق "التوافر الحيوي" (Bioavailability Audit): التأكد من أن الأدوية الحالية تصل للدم بالتركيز المطلوب بناءً على البيئة الكيميائية الحالية للمعدة.
رصد التفاعلات اللحظية: منع التفاعلات الخطيرة بين الأدوية والمدخول الغذائي الراهن (مثل الكالسيوم مع أدوية الغدة، أو التيامين مع أدوية الاكتئاب).
مواءمة "التمثيل الغذائي": ضمان عدم حدوث خلل (مثل هبوط السكر) نتيجة تعارض أدوية السكري مع حميات "نشطة" (مثل الكيتو أو الصيام المتقطع).
2- قواعد الترجيح الزمني (Temporal Decay Logic):
فئة النظام النشط (آخر 30 يوماً): تُعامل بوزن كامل (100%)؛ لأنها تعكس الحالة الفسيولوجية الراهنة للامتصاص والتمثيل الغذائي.
فئة النظام الفاتر (من 31 إلى 90 يوماً): يُخفض وزنها إلى (30%)؛ وتُستخدم فقط كمؤشر على "عادات المريض" وليس كعامل حسم في التحذير الحرج، مع إعطاء تنبيه L5 لتحديث البيانات.
فئة النظام المنتهي (> 90 يوماً): تفقد قيمتها التحليلية تماماً (0%)؛ حيث يُفترض أن كيمياء الجسم والنمط الغذائي قد تغيرا، ويُمنع بناء أي تحذير (L1-L4) عليها لتجنب "الإنذارات الكاذبة".
الحميات العلاجية المستديمة (مرضى الكلى/القلب): تظل بوزن (100%) كإطار ثابت طالما لم يقم المريض بتغيير حالتها إلى "متوقف".
3- الربط التقاطعي الإلزامي:
(المحور 14) + (المحور 1 - الأدوية الحالية): فحص التداخل بين العناصر الـ 34 والأدوية؛ بشرط أن يكون تاريخ تسجيل النظام الغذائي ضمن الـ 30 يوماً الأخيرة.
(المحور 14) + (المحور 4 - التحاليل النشطة): ربط مستويات المعادن في الدم (البوتاسيوم، الكالسيوم) بالمدخول الغذائي الأخير لتفسير أي انحراف مخبري.
(المحور 14) + (المحور 6 - الأمراض المزمنة): التأكد من أن "الغذاء الحالي" لا يُبطل مفعول الدواء (مثل مريض ضغط يتناول صوديوم عالٍ في نظامه المسجل حديثاً).
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): خطر تسمم دوائي أو فشل علاجي ناتج عن غذاء مسجل خلال 30 يوماً (مثل عصير الجريب فروت مع أدوية القلب).
تنبيه L2 (مرتفع): تعارض يمنع امتصاص الأدوية الحالية (مثل الألبان مع أدوية الغدة) بناءً على نمط غذائي حديث.
تنبيه L3 (متوسط): الغذاء الحالي يؤدي لزيادة الآثار الجانبية المزعجة (مثل الكافيين مع أدوية الربو).
تنبيه L4 (منخفض): توصية بتنظيم توقيت الوجبات (فصل الدواء عن الطعام بساعتين) بناءً على النمط الغذائي الحالي.
تنبيه L5 (احترازي - استباقي): (الأهم هنا) يصدر عند وجود نظام غذائي تجاوز 30 يوماً؛ حيث يخبر المريض: "نظامك الغذائي المسجل قديم، يرجى تحديثه لضمان دقة تحليل أمان أدويتك الحالية".
المحور (15): الفيتامينات والمكملات الغذائية
1- الهدف التحليلي:
رصد "التداخل الدوائي-التكميلي" النشط: التأكد من أن المكملات التي يتناولها المريض "الآن" لا تبطل مفعول أدوية المحور (1) أو تسبب سمية تراكمية (مثل فيتامين K الذي يبطل مفعول "الواريفرين"، أو نبتة سانت جون التي تسرع استقلاب أدوية القلب والاكتئاب مما يفقدها مفعولها).
تحليل "العبء الأيضي الكلي": تقييم مدى إرهاق الكبد والكلى نتيجة معالجة "كوكتيل" من المكملات والأدوية في آن واحد، ورصد ما إذا كان هذا العبء يظهر في تحاليل المحور (4).
التدقيق النوعي (تعويضي vs تحسيني): التمييز بين المكملات الضرورية لعلاج نقص مثبت مخبرياً، والمكملات "التحسينية" التي قد تمثل خطراً غير مبرر عند تداخلها مع الأدوية.
2- قواعد الترجيح الزمني (Temporal Logic):
المكملات المستمرة (> 3 أشهر): تُعامل بوزن كامل (100%) لأنها أحدثت تغييراً ثابتاً في الكيمياء الحيوية للجسم وتخزين المعادن.
الكورسات القصيرة والمنتهية: يُطبق مبدأ "فترة الغسيل" (Washout Period)؛ يظل وزنها (100%) لمدة 30 يوماً بعد آخر جرعة، ثم يسقط لـ (0%)، إلا في الفيتامينات الذائبة في الدهون (A, D, E, K) حيث يظل وزنها (50%) لمدة 90 يوماً نظراً لتخزينها في أنسجة الجسم.
المكملات المجهولة التاريخ: تُعامل بوزن (100%) كإجراء احترازي حتى يتم تحديث بياناتها (تطبيق قاعدة L5).
3- الربط التقاطعي الإلزامي:
(المحور 15) + (المحور 1 - الأدوية الحالية): التدقيق الفوري في التداخلات الكيميائية (مثل الزنك أو الكالسيوم الذي يمنع امتصاص بعض المضادات الحيوية أو أدوية الغدة المسجلة حالياً).
(المحور 15) + (المحور 4 - التحاليل النشطة <= 30 يوماً): مقارنة جرعة المكمل مع مستواه الفعلي في الدم؛ فإذا كانت الجرعة عالية والمستوى مرتفع، يتم تفعيل تحذير "السمية التراكمية".
(المحور 15) + (المحور 14 - الغذاء): حساب "المدخول الكلي" من العنصر (غذاء + مكمل) ومقارنته بالحد الأقصى المسموح (UL) لتجنب الآثار الجانبية للأدوية الحالية.
4- محفزات التحذير (Scoring Logic):
تنبيه L1 (حرج): اكتشاف تداخل مباشر وخطير بين مكمل حالي ودواء في المحور (1) (مثل تناول مكملات "نبتة سانت جون" مع أدوية منع التجلط أو أدوية الصرع، مما يهدد بفشل علاجي كارثي).
تنبيه L2 (مرتفع): المكمل الغذائي يمنع امتصاص أحد أدوية المحور (1) تماماً أو يضاعف آثاره الجانبية بشكل رصده البروتوكول (مثل فيتامين E بجرعات عالية مع أدوية السيولة مما يرفع خطر النزيف التلقائي).
تنبيه L3 (متوسط): الحاجة لفصل زمني (ساعتين على الأقل) بين المكمل ودواء حالي لضمان الامتصاص، وهو خلل تنظيمي يكتشفه الـ AI في جدول المريض.
تنبيه L4 (منخفض): توصية بتقليل جرعات المكملات "التحسينية" غير الضرورية لتقليل العبء على الكلى/الكبد في ظل وجود أدوية قوية في المحور (1).
تنبيه L5 (احترازي): نصيحة بتحديث بيانات المكملات أو إجراء تحاليل لنسب الفيتامينات لأن البيانات المسجلة تجاوزت مدة صلاحيتها الزمنية.
المحور (16): النشاط الرياضي 
الهدف التحليلي:
تحليل "معدل التخلص الدوائي" (Clearance Audit): تقييم كيف تؤدي الرياضة المكثفة (بناءً على معاملات MET المسجلة لكل رياضة) إلى تسريع استقلاب الأدوية الحالية وتغيير "عمر النصف" لها في الدم.
رصد مخاطر "الانهيار العضلي" (Rhabdomyolysis): التحذير من التآزر السلبي بين الرياضات عالية الشدة (High MAT) وأدوية المحور (1) التي تؤثر على الألياف العضلية (مثل أدوية الكوليسترول Statins أو بعض المضادات الحيوية).
تدقيق "حجم التوزيع" (Volume of Distribution): فهم كيف يؤثر "البناء العضلي" والكتلة الحيوية للمريض على تركيز الأدوية الحالية في أنسجة الجسم المختلفة.
2- قواعد الترجيح الزمني (Temporal Logic):
الخطة الرياضية النشطة: تُعامل بوزن كامل (100%) طوال فترة التاريخ المحدد للبداية والنهاية، بشرط وجود ممارسة فعلية موثقة.
قاعدة "تراجع اللياقة" (الـ 14 يوماً): إذا انتهت الخطة أو توقف النشاط منذ أكثر من 14 يوماً دون تجديد، ينخفض وزن التأثير فوراً إلى (20%)؛ لأن التغيرات الفسيولوجية (مثل حساسية الإنسولين وسرعة الحرق) تبدأ في العودة لمستويات الركود، ويُفعل التنبيه L5 لطلب تحديث الحالة البدنية.
التاريخ الرياضي القديم (> 30 يوماً): يُعامل كبيانات مرجعية (0%) ولا يُبنى عليه أي تحذير طبي حالي.
3- الربط التقاطعي الإلزامي:
 (المحور 16) + (المحور 1 - الأدوية الحالية): الربط بين شدة التمرين (MET/MAT) والآثار الجانبية للأدوية (مثل خطر الجفاف مع مدرات البول، أو هبوط السكر مع الإنسولين أثناء التمرين).
