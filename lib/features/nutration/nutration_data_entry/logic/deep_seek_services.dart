قواعد الـ keywords:
- قاعدة الهوية الغذائية: الـ keyword هي الاسم الجذري للمادة كطعام مستقل — مجردة من أوصاف الحالة (Fried, Sliced). يُحظر التجريد للمصدر الزراعي (القمح، الصويا) إذا كان للمادة اسم غذائي متعارف عليه.
- توجيه حسم الهوية: يُمنع استخدام كلمات عامة (Oil, Fat) — يجب كتابة النوع تحديداً (Sunflower, Corn, Olive). الافتراضي للزيوت العربية = Sunflower ما لم يُنص على غير ذلك.
- التمييز الجبري: يُحظر تكرار نفس الـ keyword لأكثر من مكون في الوجبة. الـ keyword تتطابق مع المادة الفيزيائية في السطر فقط (في ساندوتش الطعمية: Falafel وليس Sandwich).
- إلزامية المصفوفة: يُحظر استخدام keyword مفردة — يجب استخدام keywords (مصفوفة) بحد أدنى 2 وحد أقصى 5 مرادفات مرتبة من الأعلى يقيناً للأدنى.
ثالثاً: مصفوفة الفلاتر الثمانية:
1. Prep_state (قاعدة 6، 9)
2. Source_Type (قاعدة 1، 3)
3. WWEIA_Category (قاعدة 7)
4. Cook_Method (قاعدة 9)
5. Part_Used (قاعدة 5، 6)
6. Additives (قاعدة 4، 10)
7. Fat_Level (قاعدة 1، 3)
8. Enrichment (قاعدة 1، 3)
رابعاً: ميثاق الـ61 خياراً (Zero Hallucination Policy):
- الحظر المطلق: يُحظر استخدام أي قيمة خارج الجدول المعتمد.
- الأقرب تقنياً: عند غياب مطابقة 100% → الأقرب هيكلياً من الجدول.
- لغة المخرج: قيم الفلاتر بالإنجليزية حصراً.
خامساً: قيد التمييز النوعي (Qualitative Distinction):
الفلاتر الثمانية يجب أن تكون "وصفاً إقصائياً" — لا ينطبق إلا على مادة واحدة في USDA. مثال: دجاج → لا يكفي Poultry، بل يجب تحديد Part_Used (Breast) + Fat_Level (Low Fat).
سادساً: قيد الفرادة المطلقة (Single Hit Constraint):
1. منطق التدعيم (Enrichment Logic):
   - Enriched إجبارياً: للمواد المعالجة والمطحونة (دقيق أبيض، مكرونة، خبز فينو/توست).
   - Not Enriched إجبارياً: لكل ما هو في صورته الطبيعية (أرز، لحوم، خضروات، بقوليات، زيوت).
   - - Standard إجبارياً: للدواجن والأسماك والبيض  والألبان — لأن USDA يصنفها كـ Standard.
2. منطق القوام والتركيب (Structure Logic):
   - Composite / Dish: لكل خليط مطبوخ فقد هويته المفردة (بشاميل، حشوات، مخبوزات).
   - Flesh أو Seeds/Grains أو Liquid: للمكونات الصافية غير المختلطة.
- يُحظر استخدام Puree/Infant Formula للصلصات والمرقات والسوائل.
الصحيح: Liquid/Beverage للسوائل | Composite/Dish للصلصات المطبوخة.
هذا الخطأ = مخالفة لجدول الـ61 خياراً.
- اللحم المفروم = Flesh (مادة صافية لم تختلط كيميائياً) —   يُحظر استخدام Composite/Dish للحوم المفرومة.
"مثال خاطئ: طحينة → Puree / Infant Formula ❌ | الصحيح: طحينة → Seeds / Grains ✅ — يُحظر استخدام Puree / Infant Formula لأي صنف غذائي بالغ سواء كان معجوناً أو كريمياً أو سائلاً — Puree / Infant Formula محجوزة حصراً لأغذية الأطفال في USDA"
3. منطق الدهون (Fat Logic):
   - يُحظر Standard في فلتر الدهون إلا عند انعدام المعلومة تماماً.
   - الأرز المسلوق = Low Fat | الصلصات البيضاء/المحاشي/اللحم المفروم = Moderate Fat | الزيوت/القلي = High Fat.
4. معيار التركيب البشري: Composite / Dish إجبارياً لكل ما تمت معالجته يدوياً أو آلياً (طعمية، كفتة، خبز، حلويات، صلصات). يُحظر Whole إلا للثمار والحبوب بهيئتها الطبيعية.
